package org.hidetake.util.oauth.config;

public class NoSuchNodeException extends Exception
{

	private static final long serialVersionUID = 1L;

	public NoSuchNodeException()
	{
		super();
	}

	public NoSuchNodeException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public NoSuchNodeException(String arg0)
	{
		super(arg0);
	}

	public NoSuchNodeException(Throwable arg0)
	{
		super(arg0);
	}
	
}
