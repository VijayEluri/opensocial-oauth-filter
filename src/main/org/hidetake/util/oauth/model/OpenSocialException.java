package org.hidetake.util.oauth.model;

public class OpenSocialException extends Exception
{

	private static final long serialVersionUID = 1L;

	public OpenSocialException()
	{
		super();
	}

	public OpenSocialException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

	public OpenSocialException(String arg0)
	{
		super(arg0);
	}

	public OpenSocialException(Throwable arg0)
	{
		super(arg0);
	}

}
